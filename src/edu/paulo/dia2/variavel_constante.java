package edu.paulo.dia2;

public class variavel_constante {
    public static void main(String[] args) throws Exception {
        
        //Variavel
        int numero = 5;
        numero = 10;
        System.out.println(numero);

        //constante
        final float pi = 3.14F;
        System.out.println(pi);

    }
    
}
